// Arith.java
// Program that practices Arithmetic operators
import java.util.Scanner; // program uses class Scanner

public class Arith
{
   public static void main( String[] args )
   {
      System.out.print("7+3=");
      System.out.println(7+3);
   
      System.out.print("7-3=");
      System.out.println(7-3);
   
      System.out.print("7*3=");
      System.out.println(7*3);
   
      // division using double or float
      System.out.print("7/3=");
      System.out.println(7/3);
   
      // remainder
      System.out.print("7%3=");
      System.out.println(7%3);
   } // close method main
} // close class Arith
   
